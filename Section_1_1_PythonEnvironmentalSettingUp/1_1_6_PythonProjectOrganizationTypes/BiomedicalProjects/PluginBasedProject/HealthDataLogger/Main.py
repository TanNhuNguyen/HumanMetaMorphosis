#*********************************************************************************************************#
#********************************************* SUPPORTING LIBRARIES **************************************#
#*********************************************************************************************************#
from Core.Logger import getSampleData;
from Core.PluginManager import loadPlugins;

#*********************************************************************************************************#
#********************************************* SUPPORTING FUNCTIONS **************************************#
#*********************************************************************************************************#
def main():
    data = getSampleData()
    plugins = loadPlugins()
    for plugin in plugins:
        plugin.run(data);

#*********************************************************************************************************#
#********************************************* MAIN FUNCTION *********************************************#
#*********************************************************************************************************#
if __name__ == "__main__":
    main()