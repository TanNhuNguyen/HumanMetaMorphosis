def getAndProcessTexts():
    # Initializing
    print("main::getAndProcessTexts:: Initializing ...");

    # Get the texts from the users
    print("main::getAndProcessTexts:: Getting the text from the user ...");
    text = input("main::getAndProcessTexts:: Please enter a text: ");

    # Load plugins
    print("main::getAndProcessTexts:: Loading plugins ...");
    plugins = loadPlugins();
    
    # Print out the loaded plugin
    print("main::getAndProcessTexts:: Printing out the loaded plugin ...");
    print("main::getAndProcessTexts:: ", plugins);

    # Process the text based on the plugin functions
    print("main::getAndProcessTexts:: Processing the text ...");
    for name, pluginFunction in plugins:
        result = pluginFunction(text);
        print(f"main::getAndProcessTexts:: \t Input: {text} -> Output: {result}");

    # Finished processing.
    print("main::getAndProcessTexts:: Finished processing.");

#*********************************************************************************************************#
#********************************************* MAIN FUNCTION *********************************************#
#*********************************************************************************************************#
def main():
    getAndProcessTexts();
if __name__ == "__main__":
    main();